package setup;

import data.Ingredient;

public class StockElementManager {
	
	private Ingredient tomate;
	private Ingredient oeuf;
	private Ingredient steak;
	private Ingredient pain;
	private Ingredient fromage;

	public StockElementManager(Ingredient tomate, Ingredient oeuf, Ingredient steak, Ingredient pain,
			Ingredient fromage) {
		super();
		this.tomate = tomate;
		this.oeuf = oeuf;
		this.steak = steak;
		this.pain = pain;
		this.fromage = fromage;
		
	}

}
